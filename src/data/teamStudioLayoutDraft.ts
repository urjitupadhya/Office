import type { LayoutEditorElement } from "./layoutEditorTypes";

type TeamStudioDraftElement = Omit<LayoutEditorElement, "assetId">;

type AssetPreset = Pick<
  TeamStudioDraftElement,
  "src" | "sourcePath" | "category" | "group" | "renderMode" | "repeat" | "backgroundSize"
>;

type ElementOptions = Partial<
  Pick<
    TeamStudioDraftElement,
    | "src"
    | "sourcePath"
    | "category"
    | "group"
    | "renderMode"
    | "repeat"
    | "backgroundSize"
    | "opacity"
    | "cropLeft"
    | "cropTop"
    | "cropRight"
    | "cropBottom"
  >
>;

type PodSpec = {
  id: string;
  podNumber: string;
  rearChairLeft: number;
  rearChairTop: number;
  rearChairZIndex: number;
  rearOccupantLeft: number;
  rearOccupantTop: number;
  rearOccupantZIndex: number;
  deskShellLeft: number;
  deskShellTop: number;
  deskShellZIndex: number;
  workstationLeft: number;
  workstationTop: number;
  workstationZIndex: number;
  workstationAsset: "deskLaptop" | "deskPcModern";
  frontOccupantLeft: number;
  frontOccupantTop: number;
  frontOccupantZIndex: number;
  frontChairLeft: number;
  frontChairTop: number;
  frontChairZIndex: number;
};

function createElement(
  id: string,
  label: string,
  preset: AssetPreset,
  left: number,
  top: number,
  width: number,
  height: number,
  zIndex: number,
  options: ElementOptions = {},
): TeamStudioDraftElement {
  return {
    id,
    label,
    src: options.src ?? preset.src,
    sourcePath: options.sourcePath ?? preset.sourcePath,
    category: options.category ?? preset.category,
    group: options.group ?? preset.group,
    renderMode: options.renderMode ?? preset.renderMode,
    repeat: options.repeat ?? preset.repeat,
    backgroundSize: options.backgroundSize ?? preset.backgroundSize,
    left,
    top,
    width,
    height,
    zIndex,
    opacity: options.opacity ?? 1,
    cropLeft: options.cropLeft ?? 0,
    cropTop: options.cropTop ?? 0,
    cropRight: options.cropRight ?? 0,
    cropBottom: options.cropBottom ?? 0,
  };
}

function createPatternPreset(
  src: string,
  sourcePath: string,
  category: string,
  group: string,
  backgroundSize = "56px 56px",
  repeat = true,
): AssetPreset {
  return {
    src,
    sourcePath,
    category,
    group,
    renderMode: "pattern",
    repeat,
    backgroundSize,
  };
}

function createImagePreset(
  src: string,
  sourcePath: string,
  category: string,
  group: string,
): AssetPreset {
  return {
    src,
    sourcePath,
    category,
    group,
    renderMode: "image",
    repeat: false,
    backgroundSize: "contain",
  };
}

